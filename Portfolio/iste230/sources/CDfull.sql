drop database if exists cdfull;
create database cdfull;
use cdfull;
create table Artists(
  ArtistID Integer Not NUll,
  Artist varChar(50),
  PRIMARY KEY(ArtistID));
create table Songs(
  SongID Integer Not NUll,
  SongTitle varChar(50),
  Length Integer,
  ArtistID Integer Not NUll,
  PRIMARY KEY(SongID),
  FOREIGN KEY(ArtistID) REFERENCES Artists(ArtistID));
create table CDs(
  CDID Integer Not NUll,
  CDTitle varChar(50),
  PRIMARY KEY(CDID));
create table CDMakeup(
  CDID Integer Not NUll,
  SongID Integer Not NUll,
  PRIMARY KEY(SongID, CDID),
  FOREIGN KEY(SongID) REFERENCES Songs(SongID),
  FOREIGN KEY(CDID) REFERENCES CDs(CDID));

